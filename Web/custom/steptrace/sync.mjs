// steptrace build — stitches the src/ fragments into each host's single-file artifact.
// No compilation: the fragments are raw slices of ONE IIFE (shared scope), read in a
// stable order and concatenated verbatim, so the assembled engine is byte-for-byte
// what a single hand-authored file would be. Each host gets its OWN copy so the two
// plugins stay independent (neither reads the other's files at build or run time):
//   Obsidian → Vault/.obsidian/plugins/<id>/main.js   (engine + obsidian-plugin.js)
//   Quartz   → Web/quartz/static/steptrace/engine.js  (the component <script>-loads it)
// Run after editing anything under src/ or obsidian-plugin.js:  npm run steptrace:sync
// (or `npm run steptrace:watch` to re-run this automatically on every save).

import { readFileSync, writeFileSync, mkdirSync, copyFileSync, readdirSync } from "node:fs"
import { dirname, join, relative, sep } from "node:path"
import { fileURLToPath, pathToFileURL } from "node:url"

const here = dirname(fileURLToPath(import.meta.url))
const repoRoot = join(here, "..", "..", "..") // custom/steptrace → custom → Web → repo root
const srcDir = join(here, "src")

const banner =
  "// GENERATED by custom/steptrace/sync.mjs — do not edit. Edit src/ + obsidian-plugin.js.\n"

// Every .js fragment under src/, in stable path order. The two-digit filename
// prefixes ARE the concatenation order (00-prologue opens the IIFE, 90-epilogue
// closes it); the nested 40-algorithms/* files slot in between §3 and §5 by prefix.
// Ordering is plain code-unit sort on the POSIX-style relative path — deterministic
// across platforms and independent of readdir order.
export function fragmentFiles() {
  const files = []
  const walk = (dir) => {
    for (const entry of readdirSync(dir, { withFileTypes: true })) {
      const full = join(dir, entry.name)
      if (entry.isDirectory()) walk(full)
      else if (entry.name.endsWith(".js")) files.push(full)
    }
  }
  walk(srcDir)
  const rel = (p) => relative(srcDir, p).split(sep).join("/")
  return files.sort((a, b) => (rel(a) < rel(b) ? -1 : rel(a) > rel(b) ? 1 : 0))
}

// Concatenate the fragments verbatim into the assembled engine source.
export function buildEngine() {
  return fragmentFiles()
    .map((f) => readFileSync(f, "utf8"))
    .join("")
}

// Assemble both host artifacts from src/. Importable so watch.mjs can call it.
export function stitch() {
  const engine = buildEngine()
  const bootstrap = readFileSync(join(here, "obsidian-plugin.js"), "utf8")

  // 1) Obsidian plugin: engine + bootstrap concatenated into the vault plugin dir.
  //    (The folder name must match the manifest id — Obsidian loads plugins by id.)
  const manifest = JSON.parse(readFileSync(join(here, "manifest.json"), "utf8"))
  const pluginDir = join(repoRoot, "Vault", ".obsidian", "plugins", manifest.id)
  mkdirSync(pluginDir, { recursive: true })
  writeFileSync(join(pluginDir, "main.js"), banner + engine + "\n" + bootstrap)
  copyFileSync(join(here, "manifest.json"), join(pluginDir, "manifest.json"))
  // The Hot Reload community plugin watches for this marker; keep it present so a
  // freshly-synced main.js reloads without toggling the plugin by hand.
  writeFileSync(join(pluginDir, ".hotreload"), "")

  // 2) Quartz plugin: the engine's own served copy (the component loads it at runtime).
  const staticDir = join(repoRoot, "Web", "quartz", "static", "steptrace")
  mkdirSync(staticDir, { recursive: true })
  writeFileSync(join(staticDir, "engine.js"), banner + engine)

  return { manifestId: manifest.id, fragments: fragmentFiles().length }
}

// Run the stitch when invoked directly (npm run steptrace:sync); stay importable
// (no side effects) when watch.mjs pulls in stitch().
if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  const { manifestId, fragments } = stitch()
  console.log(
    `steptrace: ${fragments} src/ fragments → Vault/.obsidian/plugins/${manifestId}/main.js (Obsidian) + Web/quartz/static/steptrace/engine.js (Quartz)`,
  )
}
